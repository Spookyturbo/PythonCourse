#Computes the Area and/or Perimeter of
#a rectangle

def area(width, height):
    """Returns area of a rectangle with given dimensions"""
    return width * height

def perimeter(width, height):
    """Returns perimeter of a rectangle with given dimensions"""
    return 2 * width + 2 * height
